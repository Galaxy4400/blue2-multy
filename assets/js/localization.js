const LANGUAGE_LIST = ['cs', 'de', 'dk', 'el', 'en', 'es', 'et', 'fi', 'fr', 'hu', 'it', 'jp', 'ko', 'lt', 'nl', 'no', 'pl', 'pt', 'ro', 'ru', 'se', 'sk'];


initLocalizationList();

initLanguage();

function initLanguage() {
	const userLang = (navigator.language || navigator.userLanguage).split('-')[0];

	const initLang = getCookie('init_lang');

	if (initLang || userLang === 'en' || !LANGUAGE_LIST.includes(userLang)) return;

	setCookie('init_lang', userLang);

	window.location.replace(`${window.location.origin}/${userLang}`);
}

//===============================================================
function local(keyString) {
	const localString = localsObject().find(({ key }) => key === keyString)?.dict[LANGUAGE];

	return localString ? localString : keyString;
}

//===============================================================
function initLocalizationList() {
	const locList = document.querySelector("[data-language-list]");

	if (!locList) return;

	const currentLanguage = getCurrentLanguage();

	LANGUAGE_LIST.forEach(language => {
		const langUrl = `${window.location.origin}/${language === 'en' ? '' : language}`;

		const activeClass = currentLanguage === language ? '_active' : '';
		
		const item = `
			<li class="language__item">
				<a class="language__link ${activeClass}" href="${langUrl}">${language}</a>
			</li>
		`;

		locList.insertAdjacentHTML('beforeend', item);
	});
}

//===============================================================
function getCurrentLanguage() {
	const fullUrl = window.location.href;
	const origin = window.location.origin;
	const path = fullUrl.slice(origin.length);
	const language = path.split('/')[1];

	return LANGUAGE_LIST.includes(language) ? language : 'en';
}

